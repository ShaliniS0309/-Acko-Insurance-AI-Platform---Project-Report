-- 2.1 USERS TABLE
CREATE TABLE users (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    name VARCHAR(100) NOT NULL,
    email VARCHAR(150) UNIQUE NOT NULL,
    phone VARCHAR(15),
    role VARCHAR(20) DEFAULT 'customer' CHECK (role IN ('customer', 'manager')),
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE INDEX idx_users_email ON users(email);
CREATE INDEX idx_users_role ON users(role);
CREATE INDEX idx_users_created_at ON users(created_at);

COMMENT ON TABLE users IS 'Stores all platform users - customers and managers';
COMMENT ON COLUMN users.role IS 'User role: customer or manager';

-- 2.2 QUOTATIONS TABLE
CREATE TABLE quotations (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    user_id UUID REFERENCES users(id) ON DELETE SET NULL,
    vehicle_type VARCHAR(10) NOT NULL CHECK (vehicle_type IN ('car', 'bike')),
    vehicle_make VARCHAR(50) NOT NULL,
    vehicle_model VARCHAR(80) NOT NULL,
    manufacturing_year INTEGER NOT NULL CHECK (manufacturing_year BETWEEN 1900 AND 2024),
    fuel_type VARCHAR(20) NOT NULL CHECK (fuel_type IN ('Petrol', 'Diesel', 'Electric', 'CNG')),
    city VARCHAR(80) NOT NULL,
    city_tier VARCHAR(10) CHECK (city_tier IN ('tier1', 'tier2', 'tier3')),
    idv DECIMAL(12,2) NOT NULL CHECK (idv > 0),
    ncb_percent INTEGER DEFAULT 0 CHECK (ncb_percent IN (0, 20, 25, 35, 45, 50)),
    vehicle_age INTEGER GENERATED ALWAYS AS (EXTRACT(YEAR FROM CURRENT_DATE) - manufacturing_year) STORED,
    predicted_premium DECIMAL(12,2) NOT NULL CHECK (predicted_premium > 0),
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE INDEX idx_quotations_user_id ON quotations(user_id);
CREATE INDEX idx_quotations_vehicle_type ON quotations(vehicle_type);
CREATE INDEX idx_quotations_city ON quotations(city);
CREATE INDEX idx_quotations_created_at ON quotations(created_at);

COMMENT ON TABLE quotations IS 'Stores insurance premium quotations generated by ML model';
COMMENT ON COLUMN quotations.idv IS 'Insured Declared Value - current market value of vehicle';
COMMENT ON COLUMN quotations.ncb_percent IS 'No Claim Bonus discount percentage';

-- 2.3 CLAIMS TABLE
CREATE TABLE claims (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    user_id UUID REFERENCES users(id) ON DELETE SET NULL,
    vehicle_type VARCHAR(10) NOT NULL CHECK (vehicle_type IN ('car', 'bike')),
    policy_number VARCHAR(50) NOT NULL,
    incident_date DATE NOT NULL,
    damage_type VARCHAR(50) NOT NULL CHECK (damage_type IN ('scratch', 'dent', 'crack', 'major_damage', 'total_loss')),
    damaged_parts VARCHAR(200),
    severity VARCHAR(20) NOT NULL CHECK (severity IN ('minor', 'moderate', 'major')),
    city VARCHAR(80),
    image_s3_key VARCHAR(255),
    form_s3_key VARCHAR(255),
    predicted_amount DECIMAL(12,2) NOT NULL CHECK (predicted_amount >= 0),
    approval_probability DECIMAL(5,4) NOT NULL CHECK (approval_probability BETWEEN 0 AND 1),
    status VARCHAR(20) DEFAULT 'pending' CHECK (status IN ('pending', 'approved', 'rejected', 'review')),
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE INDEX idx_claims_user_id ON claims(user_id);
CREATE INDEX idx_claims_vehicle_type ON claims(vehicle_type);
CREATE INDEX idx_claims_status ON claims(status);
CREATE INDEX idx_claims_incident_date ON claims(incident_date);
CREATE INDEX idx_claims_created_at ON claims(created_at);

COMMENT ON TABLE claims IS 'Stores all claim submissions with AI predictions';
COMMENT ON COLUMN claims.image_s3_key IS 'AWS S3 path for uploaded damage image';
COMMENT ON COLUMN claims.approval_probability IS 'ML model predicted approval likelihood (0 to 1)';

-- 2.4 CHAT LOGS TABLE
CREATE TABLE chat_logs (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    user_id UUID REFERENCES users(id) ON DELETE SET NULL,
    intent VARCHAR(30) CHECK (intent IN ('policy_qa', 'quotation', 'claims', 'general', 'manager_query')),
    question TEXT NOT NULL,
    answer TEXT,
    retrieved_source VARCHAR(255),
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE INDEX idx_chat_logs_user_id ON chat_logs(user_id);
CREATE INDEX idx_chat_logs_intent ON chat_logs(intent);
CREATE INDEX idx_chat_logs_created_at ON chat_logs(created_at);

COMMENT ON TABLE chat_logs IS 'Stores all chat interactions for analytics and training';

