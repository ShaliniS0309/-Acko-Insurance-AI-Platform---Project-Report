-- 10.2 Claims Funnel Analysis
WITH claim_funnel AS (
    SELECT 'Total Claims' AS stage, COUNT(*) AS count FROM claims
    UNION ALL
    SELECT 'Approved Claims', COUNT(*) FROM claims WHERE status = 'approved'
    UNION ALL
    SELECT 'Reviewed Claims', COUNT(*) FROM claims WHERE status = 'review'
    UNION ALL
    SELECT 'Rejected Claims', COUNT(*) FROM claims WHERE status = 'rejected'
)
SELECT 
    stage,
    count,
    ROUND(count * 100.0 / (SELECT COUNT(*) FROM claims), 2) AS percentage
FROM claim_funnel
ORDER BY count DESC;

-- 10.3 Customer Lifetime Value Analysis
SELECT 
    u.id,
    u.name,
    u.email,
    (SELECT COUNT(*) FROM quotations q WHERE q.user_id = u.id) AS total_quotes,
    (SELECT COUNT(*) FROM claims c WHERE c.user_id = u.id) AS total_claims,
    (SELECT COALESCE(SUM(predicted_premium), 0) FROM quotations q WHERE q.user_id = u.id) AS total_quoted_value,
    (SELECT COALESCE(SUM(predicted_amount), 0) FROM claims c WHERE c.user_id = u.id AND c.status = 'approved') AS total_claimed_value
FROM users u
WHERE u.role = 'customer'
ORDER BY total_quoted_value DESC
LIMIT 10;

-- 10.4 Risk Assessment - High Risk Claims
SELECT 
    id,
    user_id,
    vehicle_type,
    damage_type,
    severity,
    predicted_amount,
    approval_probability,
    CASE 
        WHEN predicted_amount > 100000 AND approval_probability < 0.3 THEN '🔴 Critical'
        WHEN predicted_amount > 50000 AND approval_probability < 0.5 THEN '🟠 High'
        WHEN approval_probability < 0.5 THEN '🟡 Medium'
        ELSE '🟢 Low'
    END AS risk_level
FROM claims
WHERE approval_probability < 0.6
ORDER BY risk_level, predicted_amount DESC;

-- 10.5 Geographic Heat Map Data
SELECT 
    city,
    COUNT(*) AS claims,
    ROUND(AVG(predicted_amount), 2) AS avg_payout,
    ROUND(COUNT(*) * 100.0 / (SELECT COUNT(*) FROM claims), 2) AS percentage_of_total
FROM claims
WHERE city IS NOT NULL
GROUP BY city
ORDER BY claims DESC;

