-- 6.1 Average premium by vehicle type
SELECT 
    vehicle_type,
    COUNT(*) AS total_quotations,
    ROUND(AVG(predicted_premium), 2) AS avg_premium,
    ROUND(MIN(predicted_premium), 2) AS min_premium,
    ROUND(MAX(predicted_premium), 2) AS max_premium
FROM quotations
GROUP BY vehicle_type
ORDER BY avg_premium DESC;

-- 6.2 Premium by city
SELECT 
    city,
    city_tier,
    COUNT(*) AS total_quotations,
    ROUND(AVG(predicted_premium), 2) AS avg_premium
FROM quotations
GROUP BY city, city_tier
ORDER BY avg_premium DESC
LIMIT 10;

-- 6.3 NCB discount impact
SELECT 
    ncb_percent,
    COUNT(*) AS total_quotations,
    ROUND(AVG(predicted_premium), 2) AS avg_premium
FROM quotations
GROUP BY ncb_percent
ORDER BY ncb_percent DESC;

