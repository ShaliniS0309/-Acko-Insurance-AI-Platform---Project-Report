-- 3.1 Customer Activity Summary
CREATE OR REPLACE VIEW vw_customer_activity AS
SELECT 
    u.id AS user_id,
    u.name,
    u.email,
    u.role,
    COUNT(DISTINCT q.id) AS total_quotations,
    COUNT(DISTINCT c.id) AS total_claims,
    COUNT(DISTINCT cl.id) AS total_chat_queries,
    ROUND(AVG(q.predicted_premium), 2) AS avg_quoted_premium,
    ROUND(AVG(c.predicted_amount), 2) AS avg_claim_amount
FROM users u
LEFT JOIN quotations q ON u.id = q.user_id
LEFT JOIN claims c ON u.id = c.user_id
LEFT JOIN chat_logs cl ON u.id = cl.user_id
GROUP BY u.id, u.name, u.email, u.role;

-- 3.2 Dashboard Summary
CREATE OR REPLACE VIEW vw_dashboard_summary AS
SELECT 
    (SELECT COUNT(*) FROM claims) AS total_claims,
    (SELECT COUNT(*) FROM claims WHERE vehicle_type = 'car') AS car_claims,
    (SELECT COUNT(*) FROM claims WHERE vehicle_type = 'bike') AS bike_claims,
    ROUND((SELECT AVG(predicted_amount) FROM claims), 2) AS avg_claim_amount,
    (SELECT COUNT(*) FROM claims WHERE status = 'approved') AS approved_claims,
    ROUND((SELECT COUNT(*) FILTER (WHERE status = 'approved') FROM claims) * 100.0 / NULLIF((SELECT COUNT(*) FROM claims), 0), 2) AS approval_rate,
    (SELECT COUNT(*) FROM quotations) AS total_quotations,
    ROUND((SELECT AVG(predicted_premium) FROM quotations), 2) AS avg_premium,
    (SELECT COUNT(*) FROM chat_logs) AS total_chat_interactions;

-- 3.3 BI Export View
CREATE OR REPLACE VIEW vw_bi_export AS
SELECT 
    u.id AS user_id,
    u.name AS user_name,
    u.email,
    u.role,
    c.id AS claim_id,
    c.vehicle_type,
    c.policy_number,
    c.incident_date,
    c.damage_type,
    c.damaged_parts,
    c.severity,
    c.city AS claim_city,
    c.predicted_amount AS claim_amount,
    c.approval_probability,
    c.status AS claim_status,
    c.created_at AS claim_created_at,
    q.id AS quotation_id,
    q.vehicle_make,
    q.vehicle_model,
    q.manufacturing_year,
    q.fuel_type,
    q.city AS quotation_city,
    q.idv,
    q.ncb_percent,
    q.predicted_premium,
    q.created_at AS quotation_created_at,
    EXTRACT(YEAR FROM c.created_at) AS claim_year,
    EXTRACT(MONTH FROM c.created_at) AS claim_month
FROM users u
LEFT JOIN claims c ON u.id = c.user_id
LEFT JOIN quotations q ON u.id = q.user_id;

