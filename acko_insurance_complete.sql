-- 5.1 View all chat interactions
SELECT 
    cl.id,
    u.name AS user_name,
    u.email,
    cl.intent,
    cl.question,
    cl.answer,
    cl.retrieved_source,
    cl.created_at
FROM chat_logs cl
LEFT JOIN users u ON cl.user_id = u.id
ORDER BY cl.created_at DESC
LIMIT 20;

-- 5.2 Chat statistics by intent
SELECT 
    intent,
    COUNT(*) AS total_queries,
    ROUND(COUNT(*) * 100.0 / (SELECT COUNT(*) FROM chat_logs), 2) AS percentage
FROM chat_logs
GROUP BY intent
ORDER BY total_queries DESC;

-- 5.3 Most asked questions
SELECT 
    question,
    COUNT(*) AS frequency
FROM chat_logs
GROUP BY question
ORDER BY frequency DESC
LIMIT 10;

