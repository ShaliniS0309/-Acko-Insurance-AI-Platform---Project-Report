    ROUND(AVG(approval_probability) * 100, 2) AS avg_approval_rate
FROM claims
WHERE EXTRACT(MONTH FROM created_at) = EXTRACT(MONTH FROM CURRENT_DATE)
GROUP BY vehicle_type;

